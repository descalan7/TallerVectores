    private void cmdMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdMostrarActionPerformed
        int acum=0,acum2=0;
        double contador=0,contador2=0;
        boolean primo=true;
         for (int i = 0; i < v.length; i++){
        if(v[i]%2==0){
                 acum=acum+1;
                 
             }else if(v[i]%2==1){
                 acum2=acum2+1;
             }
        
         }
        for (int j = 0; j < v.length; j++) {
                 if(v[j]%contador==0){
                     primo=false;
                 }else{
                     contador2=contador2+1;
                 }
        }
             
        
                
        txtPrimos.setText(String.valueOf(contador2));
             
         
             
             
         
         txtPares.setText(String.valueOf(acum));
         txtImpares.setText(String.valueOf(acum2));
         
         cmdCrear.setEnabled(false);
         cmdAuto.setEnabled(false);
         cmdLlenar.setEnabled(false);
         cmdMostrar.setEnabled(false);
         cmdReset.setEnabled(true);
        
             
           
    }//GEN-LAST:event_cmdMostrarActionPerformed

